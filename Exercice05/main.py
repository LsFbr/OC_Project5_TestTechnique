def sum(a, b):
    """
    Additionne deux nombres.

    Args:
        a (int): Le premier nombre à additionner.
        b (int): Le second nombre à additionner.

    Returns:
        int: La somme des deux nombres.
    """
    return a + b


def subtraction(a, b):
    """
    Soustrait deux nombres.

    Args:
        a (int): Le premier nombre à soustraire.
        b (int): Le second nombre à soustraire.

    Returns:
        int: La différence des deux nombres.
    """
    return a - b
