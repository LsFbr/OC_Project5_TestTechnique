def sum(a, b):
    """ Additionne deux nombres.

    Args:
        a (float/int): Le premier nombre à additionner.
        b (float/int): Le second nombre à additionner.

    Returns:
        float/int: La somme des deux nombres.
    """
    return a + b


def subtraction(a, b):
    """ Soustrait deux nombres.

    Args:
        a (float/int): Le premier nombre à soustraire.
        b (float/int): Le second nombre à soustraire.

    Returns:
        float/int: La différence des deux nombres.
    """
    return a - b
